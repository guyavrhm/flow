/* Circulant matrix for state multiplication */
uint8_t mix[16] = {
    0x2, 0x3, 0x1, 0x1,
    0x1, 0x2, 0x3, 0x1,
    0x1, 0x1, 0x2, 0x3,
    0x3, 0x1, 0x1, 0x2
};

/* Circulant matrix for inverse state multiplication */
uint8_t inv_mix[16] = {
    0x14, 0x11, 0x13, 0x9,
    0x9, 0x14, 0x11, 0x13,
    0x13, 0x9, 0x14, 0x11,
    0x11, 0x13, 0x9, 0x14
};

/* Round constant */
uint8_t rcon[10] = {
    0x1, 0x2, 0x4, 0x8, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
};


/*
  Addition of key and current state block.
*/
void add_round_key(uint8_t *state, uint8_t *k, uint8_t r) {

    uint8_t i, j;
    for (i = 0; i < SIDE; i++) {
        for (j = 0; j < SIDE; j++) {
            state[SIDE*i+j] ^= k[SIZE*r+SIDE*i+j];
        }
    }
}


/*
  Byte substitution according to the sbox.
  Leftmost 4-bits -> row, Rightmost 4-bits -> column
*/
void sub_bytes(uint8_t *state) {

    uint8_t i, j;

    for (i = 0; i < SIDE; i++) {
        for (j = 0; j < SIDE; j++) {
                state[SIDE*i+j] = sbox[state[i*SIDE+j]];
        }
    }
}


/*
  Byte substitution according to the inverse sbox.
  Leftmost 4-bits -> row, Rightmost 4-bits -> column
*/
void inv_sub_bytes(uint8_t *state) {

    uint8_t i, j;

    for (i = 0; i < SIDE; i++) {
        for (j = 0; j < SIDE; j++) {
                state[SIDE*i+j] = inv_sbox[state[i*SIDE+j]];
        }
    }
}


/*
  Left shift each row by its index.
*/
void shift_rows(uint8_t *state) {

    uint8_t i, j, m, tmp;

    for (i = 0; i < SIDE; i++) {
        for (m = 0; m < i; m++) {
            tmp = state[i];

            for(j = 1; j < SIDE; j++) {
                state[SIDE*(j-1)+i] = state[SIDE*j+i];
            }

            state[SIDE*(SIDE-1)+i] = tmp;
        }
    }
}


/*
  Right shift each row by its index.
*/
void inv_shift_rows(uint8_t *state) {

    uint8_t i, j, m, tmp;

    for (i = 0; i < SIDE; i++) {
        for (m = 0; m < SIDE-i; m++) {
            tmp = state[i];

            for(j = 1; j < SIDE; j++) {
                state[SIDE*(j-1)+i] = state[SIDE*j+i];
            }

            state[SIDE*(SIDE-1)+i] = tmp;
        }
    }
}


/*
  Mixed matrix multiplication of current state block with
  the pre-defined matrix 'mix'.
*/
void mix_columns(uint8_t *state) {

    uint8_t i, j, k;
    uint8_t out[SIZE];

    for (i = 0; i < SIDE; i++) {
        for (j = 0; j < SIDE; j++) {
            out[SIDE*i+j] = gmult(state[SIDE*i+0], mix[SIDE*j+0]) ^
                            gmult(state[SIDE*i+1], mix[SIDE*j+1]) ^
                            gmult(state[SIDE*i+2], mix[SIDE*j+2]) ^
                            gmult(state[SIDE*i+3], mix[SIDE*j+3]);
        }
    }

    memcpy(state, out, sizeof(uint8_t)*SIZE);

}


/*
  Mixed matrix multiplication of current state block with
  the pre-defined matrix 'inv_mix'.
*/
void inv_mix_columns(uint8_t *state) {

    uint8_t i, j, k;
    uint8_t out[SIZE];

    for (i = 0; i < SIDE; i++) {
        for (j = 0; j < SIDE; j++) {
            out[SIDE*i+j] = gmult(state[SIDE*i+0], inv_mix[SIDE*j+0]) ^
                            gmult(state[SIDE*i+1], inv_mix[SIDE*j+1]) ^
                            gmult(state[SIDE*i+2], inv_mix[SIDE*j+2]) ^
                            gmult(state[SIDE*i+3], inv_mix[SIDE*j+3]);
        }
    }

    memcpy(state, out, sizeof(uint8_t)*SIZE);

}


/*
  AES key expansion.
*/
uint8_t *key_expansion(uint8_t *key) {

    uint8_t r, i, j;
    uint8_t tmp[SIDE];
    uint8_t *k = malloc(SIZE*(ROUNDS+1));

    memcpy(k, key, sizeof(uint8_t)*SIZE);

    for (r = 0; r < ROUNDS; r++) {

        tmp[0] = key[SIDE*(SIDE-1)+1];
        tmp[1] = key[SIDE*(SIDE-1)+2];
        tmp[2] = key[SIDE*(SIDE-1)+3];
        tmp[3] = key[SIDE*(SIDE-1)+0];

        for (i = 0; i < SIDE; i++) {
            tmp[i] = sbox[tmp[i]];
        }

        tmp[0] ^= rcon[r];

        for (i = 0; i < SIDE; i++) {
            key[SIDE*0+i] ^= tmp[i];
            key[SIDE*1+i] ^= key[SIDE*0+i];
            key[SIDE*2+i] ^= key[SIDE*1+i];
            key[SIDE*3+i] ^= key[SIDE*2+i];
        }

        memcpy(k+SIZE*(r+1), key, sizeof(uint8_t)*SIZE);

    }

    return k;
}

uint8_t *aes_init(uint8_t *key) {
    return key_expansion(key);
}

/*
  AES encryption operation.
*/
void aes_encrypt(uint8_t *data, uint8_t *k) {
    
    uint8_t r;

    add_round_key(data, k, 0);

    for (r = 1; r < ROUNDS; r++) {
        sub_bytes(data);
        shift_rows(data);
        mix_columns(data);
        add_round_key(data, k, r);
    }

    sub_bytes(data);
    shift_rows(data);
    add_round_key(data, k, ROUNDS);

}

/*
  AES decryption operation (inverse encryption).
*/
void aes_decrypt(uint8_t *cipher, uint8_t *k) {

    uint8_t r;

    add_round_key(cipher, k, ROUNDS);
    inv_shift_rows(cipher);
    inv_sub_bytes(cipher);

    for (r = 0; r < ROUNDS-1; r++) {
        add_round_key(cipher, k, ROUNDS-1-r);
        inv_mix_columns(cipher);
        inv_shift_rows(cipher);
        inv_sub_bytes(cipher);
    }

    add_round_key(cipher, k, 0);

}
